	PRIMARY KEY (`id`)
);
-- 11
CREATE TABLE `department_type` (
	`id` INT NOT NULL AUTO_INCREMENT,
	`name` VARCHAR(45) NOT NULL,
    `description` VARCHAR(45) NOT NULL,
	PRIMARY KEY (`id`)
);


